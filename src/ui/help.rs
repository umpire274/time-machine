    print_line_with_delay("Due to a fracture in the timeline, these fragments were scattered across human history:", delay_ms);
    print_line_with_delay("", delay_ms);
    print_line_with_delay("• One in Ancient Egypt.", delay_ms);
    print_line_with_delay("• One in Ancient Rome.", delay_ms);
    print_line_with_delay("• One in the Middle Ages.", delay_ms);
    print_line_with_delay("• One in the Renaissance.", delay_ms);
    print_line_with_delay("• One in Europe during the Second World War.", delay_ms);
    print_line_with_delay("", delay_ms);
    print_line_with_delay("Only by recovering and reuniting all five fragments of the Noks can TMO unlock the", delay_ms);
    print_line_with_delay("full potential of the Time Machine and overcome the limitation of time-bound journeys.", delay_ms);
    print_line_with_delay("", delay_ms);
